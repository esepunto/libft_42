#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	char	*bridge;

	if (!s1)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len - 1]))
		len--;
	bridge = (char*)malloc(sizeof(char) * (len + 1));
	if (!bridge)
		return (NULL);
	bridge[len] = '\0';
	return (ft_memcpy(bridge, s1, len));
}
